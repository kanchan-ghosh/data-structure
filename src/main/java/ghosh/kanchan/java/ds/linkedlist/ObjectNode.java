package ghosh.kanchan.java.ds.linkedlist;


public class ObjectNode {

    private Object data;
    private ObjectNode next;

    public Object getData() {
        return data;
    }

    public void setData(Object data) {
        this.data = data;
    }

    public ObjectNode getNext() {
        return next;
    }

    public void setNext(ObjectNode next) {
        this.next = next;
    }
}
